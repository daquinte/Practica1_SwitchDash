package es.ucm.gdv.interfaces;

// Esta clase esta en el modulo de interfaces debido a su uso en las propias interfaces
public class Rect {

    public Rect (int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    public int x;
    public int y;
    public int width;
    public int height;
}
